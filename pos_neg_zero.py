#Program to check whether a number is positive or negative or zero.
n=int(input())
if n==0:
  print("Zero")
elif n>0:
  print("Positive")
elif n<0:
  print("Negative")
else:
  print("Invalid Input")
  
  
#Program to check whether a number is odd or even.
n=int(input())
if n%2==0:
  print("Even")
elif n%2!=0:
  print("Odd")
else:
  print("Invalid Input")
